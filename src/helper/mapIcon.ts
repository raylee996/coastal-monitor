/**
 * 获取地图雷达船舶图标
 * @param params riskLevel风险等级；caseBottom是否有案底；isShip是否船舶雷达目标1是0否
 * @returns L.divIcon
 */
export const getMapRadarTargetIcon = ({
  riskLevel,
  caseBottom,
  heading,
  isShip
}: {
  riskLevel: number,
  caseBottom: number,
  heading?: number
  isShip?: number
}) => {
  let src: string
  switch (riskLevel) {
    case 1:
      src = radarHighSrc
      break;
    case 2:
      src = radarMiddleSrc
      break;
    case 3:
      src = radarLowSrc
      break;
    case 4:
      src = caseBottom === 1 ? radarWarnSrc : radarSrc
      break;
    default:
      src = radarSrc
      break;
  }
  const imgEle = document.createElement('img')
  imgEle.src = src

  if (isShip === 0) {
    imgEle.width = 12
    imgEle.height = 12

    if (heading) {
      imgEle.style.transform = `rotate(${heading}deg)`
    }
  } else {
    imgEle.width = 24
    imgEle.height = 12
  }

  return L.divIcon({ html: imgEle, iconSize: [14, 14] })
}

